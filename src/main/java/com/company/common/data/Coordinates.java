package com.company.common.data;

public class Coordinates {
    private float x = 0;
    private double y = 0.0;

    public void setX(float x){
        this.x = x;
    }
    public void setY(double y){
        this.y = y;
    }
    public float getX(){return x;}
    public double getY(){return y;}
}
